													<p class="mb-0 font-weight-semibold"><b>4 - Chokai (ou Chonaikai, Jichikai) e Tonarigumi</b></p>
													<div>
														<div>
															<p class="mb-0">Uma associação da região e a de vizinhanças. O tonarigumi formado de vizinhanças (na base de famílias). O chôkai é constituído de todos os tonarigumis existentes na localidade que tem o mesmo nome de tal “chô”. As principais funções de chôkai são os seguintes:</p>
															<ul>
																<li>Intermediação de fluxos de informação da prefeitura a moradores.</li>
																<li>Melhoramento de condições de vida do dia—a—dia dos moradores, promovendo atividade festiva/esportiva e a cooperação mútua; Omatsuri, Undôkai, Hanamikai, ryokô; e Kinrôhôshi, etc.</li>
															</ul>

															<p class="mb-0">Normalmente, para a participação nessas associações exige—se pequena contribuição mensal. Os associados, através dela, podem cultivar amizades e bons relacionamentos entre moradores, possibilitando—lhes uma facilidade de receber orientação e auxílio de toda natureza.</p>
															<p class="mb-0">Na vida de estrangeiros no Japão, devido a divergência de costumes, do Japão e seus países de origem, o tratamento de lixo doméstico é sempre um dos maiores problemas. A seguir explanaremos a regra padrão. Como cada localidade tem regras especificas, é melhor receber orientação do vizinho sobre a
																particularidade da região.</p>

															<p class="mb-0 font-weight-semibold"><b>Regras sobre o tratamento de lixo doméstico</b></p>

															<p class="mb-0 font-weight-semibold"><b>a) Coletas</b></p>
															<p class="mb-0">Deve respeitar o local de colocação de lixos a serem recolhidos, horário e dias da semana, para cada tipo de lixo (não se deve levar os lixos muito antes da hora marcada, para evitar que os animais os espalharem).</p>

															<p class="mb-0 font-weight-semibold"><b>b) Classificação de lixos</b></p>
															<p class="mb-0">Normalmente, separam-se em 3 (três) tipos: </p>
															<ul>
																<li>Lixos inflamáveis (papéis, madeiras, tecido e comidas)</li>
																<li> Lixos não inflamáveis (metais, vidros, plásticos e couros/calças)</li>
																<li>Lixos grandes (móveis, aparelhos eletro-eletrônicos)</li>
															</ul>

															<p class="mb-0 font-weight-semibold"><b>c) Embalagem</b></p>
															<p class="mb-0">Colocar lixos separados conforme critério anteriormente explanado, em sacos de lixos (vendidos em supermercados, etc) ou em sacos de plásticos de compra. Ou embalagem própria com tampa. Para os lixos de cozinha, retirar o liquido e vedar bem o saco. Os lixos que possam causar acidentes não devem ser misturados no saco. Quando jogar os lixos grandes, combine com a prefeitura o dia e a hora. Dependendo do volume, há cobrança da taxa de recolhimento.</p>


