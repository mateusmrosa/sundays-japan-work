																<li>Após aprovados os documentos apresentados, deve—se submeter ao exame de vista e condição física. Dependendo da província, a carteira de habilitação pode ser liberada no mesmo dia do exame, em outras, poderá demorar uma semana. Caso tiver dificuldade no idioma, convém ir ao exame acompanhado de quem possa ajudá—lo. Independentemente de sua carteira ser amador ou profissional, recebe-se somente a de amador. Mais tarde deve se submeter ao exame prático para alteração de categoria.</li>
															</ul>

														</ul>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 5.2 - Quem não possui a carteira brasileira de habilitação</li>
														<p class="mb-0">Prestando e passando no exame de motorista, teoricamente qualquer pessoa pode receber a carteira japonesa de habilitação. Mas na prática, como linguagem a ser usada nos testes japonês ou inglês (só duas línguas no momento), é dificílimo para falantes de demais línguas. Há auto—escolas autorizadas e para quem concluí o curso será dispensado do exame prático. A despesa do curso completo pode custar de ¥ 300 a 400 mil ienes.</p>

													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 5.3 - Prazo de validade da carteira</li>
														<p class="mb-0">Válido até 3º (terceiro) aniversário de nascimento após a emissão da carteira. A renovação de carteira pode ser solicitada a partir de um mês antes do vencimento, à delegacia de polícia local. A renovação é relativamente fácil e simples para quem não tem infringido o código de trânsito, e sem acidente.</p>

													</ul>

												</div>


												<div>
													<p class="mb-0 font-weight-semibold"><b>6 - Regras de trânsito no Japão:</b></p>
													<ul>
														<li class="mb-0 font-weight-semibold"> 6.1 - Três destaques no trânsito do Japão</li>
														<ul>
															<li>A mão de direcão para o carro é a esquerda e para o pedestre é a direita.</li>
															<li>A preferência no transito é sempre para pedestre.</li>
															<li>Não guiar carros depois de tomar bebidas alcoólicas.</li>
														</ul>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 6.2 - Acidentes no trânsito</li>
														<p class="mb-0">Ultimamente tem acontecido acidentes de trânsito envolvendo os estrangeiros notadamente “dekasseguis”.</p>
														<p class="mb-0">Aonde acontece o maior numero de acidentes no Japão? </p>
														<ul>
															<li>Cruzamentos com farol de pisca—pisca. Motivo: falta observar ambos os lados .</li>
															<li>Excesso de velocidade, tanto na cidade como na estrada. Motivo: as ruas e estradas são mais estreitas do que as do Brasil.</li>
															<li>Ultrapassagens forçadas. Motivo: falta de respeito aos outros.</li>
														</ul>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 6.3 - Garagem Obrigatória</li>
														<p class="mb-0">Ao adquirir o veiculo, exige—se a comprovação de ter garagem, seja própria ou alugada.
															É terminantemente proibido deixar o carro estacionado na via pública, a não ser por curto espaço de tempo.</p>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 6.4 - Vistoria obrigatória de viaturas (SHAKEN) - Sistema inexistente no Brasil</li>
														<ul>
															<li>Tendo em vista segurança no trânsito, é obrigatória a vistoria periódica para todos os veículos que transitam nas vias públicas, feita nas oficinas autorizadas pelo Ministério de Transportes.</li>
															<li>O veiculo recebe um atestado e adesivo, e o último deve ser afixado na parte superior do para-brisa.</li>
															<li>O prazo de validade desta vistoria é variado, dependendo do tipo de veiculo sendo de 1 a 2 anos. A 1ª (primeira) vistoria para o carro novo ocorre 3 (três) anos depois do faturamento.</li>
														</ul>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 6.5 - Costumes dos pedestres no Japão</li>
														<ul>
															<li>Jamais cruzar as ruas/ estradas com o semáforo vermelho, mesmo que não venha carro algum em sua direção.</li>
															<li>Utilizar a passarela e faixas de pedestre, mesmo que tenha que dar volta.</li>
														</ul>
													</ul>

												</div>

												<div>
													<p class="mb-0 font-weight-semibold"><b>7 - Depósito em bancos/correio e remessa ao Brasil:</b></p>
													<ul>
														<li class="mb-0 font-weight-semibold"> 7.1 - Prevenção contra roubo/perda / Abertura de contas</li>
														<p class="mb-0">Não deixe muito dinheiro em casa/alojamento, faca depósito em banco ou no correio, para se proteger das eventuais perdas do mesmo. Para abertura de conta no banco ou no correio leve consigo o seu passaporte ou carteira de estrangeiro. O Futsû—Yok1n (conta corrente ordinário) é comum no Japão. Não há conta para poupança, como a caderneta de poupança no Brasil. Ao abrir a conta, o funcionário perguntara se quer usar o cartão ou não. Se a resposta for positiva, o cartão sera fornecido sem despesa, e entregue a domicílio por correio.</p>
														<p class="mb-0">O uso de cartão é igual ao sistema do Brasil. O atendimento ao público no Japão, começa às 09:00 e termina às 15:00, nos dias úteis. Aos sábados, domingos e feriados não hã expediente. A máquina de sacar dinheiro funciona até mais tarde que o de atendimento no banco, porém hã cobrança de taxas extras dependendo da hora do saque. É aconselhável receber informações exatas antes de movimentar as contas.</p>

													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 7.2 - Diferença existente no sistema Japão X Brasil</li>
														<p class="mb-0">No Japão, a taxa de juros é baixa, até insignificante. Para o pagamento do dia-a-dia, usa-se o dinheiro, as vezes o cartão, não existe o costume de pagar em cheque, como no Brasil, a não ser o pagamento de empresa.<br><br>
															Como aqui no Brasil, é comum no Japão, o sistema de débito automático das contas de luz, gás e telefone, e o recebimento de ordenado creditado na conta.<br><br>
															É recomendável a adesão ao sistema, para evitar a ida ao banco e eventual esquecimento de quitar as contas. As companhias de luz e gás também fornecem-lhe o formulário para isso.
														</p>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 7.3 - Remessa de ao Brasil</li>
														<p class="mb-0">As moedas a serem utilizadas na remessa para o Brasil, normalmente, são dólares dos EUA ou iene japonês. Não há vantagem nem desvantagem na escolha de moeda. <br>
															Para sua remessa em moedas estrangeiras, é aplicada a taxa de câmbio de venda do mercado livre.<br>
															O preenchimento do pedido de remessa deve ser correto nos seguintes dados:
														</p>
														<ul>
															<li>A) Nome do remetente, endereço e telefone</li>
															<li>B) Nome do beneficiário, endereço completo, além destes,</li>
															<li>1. o nome do banco com o qual mantem sua conta,</li>
															<li>2. o nome da agência, e</li>
															<li>3. o numero da conta</li>
															<li>C) Motivo da remessa, dependendo da natureza da operação, pode sujeitar ao pagamento do IR na parte de beneficiário.</li>
															<li>4. Nem toda agência bancária no Japão opera com cambio. É melhor levar consigo um modelo preenchido para facilitar trabalho dos funcionários que lhe atendem.</li>
															<li>5. Para a remessa, utilize sempre o caminho correto, i.e., via bancária, o caminho facilitado (serviço particular) pode oferecer—lhe dor de cabeça, como demora até não recebimento de dinheiro pelo destinatário.</li>
														</ul>
													</ul>

												</div>

												<div>
													<p class="mb-0 font-weight-semibold"><b>8 - Correio (Yobin-Kyoku em japonês):</b></p>
													<ul>
														<li class="mb-0 font-weight-semibold"> 8.1 - Yobin-Chokin</li>
														<p class="mb-0">O correio, um órgão do governo, também aceita o depósito do público. A sistemática é quase idêntica ao depósito bancário.</p>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 8.2 - Yobin (Cartas) e tarifas postais</li>
														<p class="mb-0">No Japão alteração de tarifas pode haver uma vez a cada 4 ou 5 anos. Selos postais podem ser adquiridos, não só no correio mas também nas tabacarias que mantêm letreiro de venda de selos. Apôs seladas, colocá—las no POST (caixa coletora de correio) existentes nas ruas, o sistema idêntico
															ao do Brasil. A tarifa da carta aérea para o Brasil é de ¥ 120 até 10g. por volta de 1 US$ , valor vigente no Brasil. <br>
															Para o envelope fora do normal ou mais pesado consulte antes os encarregados. A falta de selo ou tarifa insuficiente pode resultar na devolução da carta ao remetente.
														</p>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 8.3 - Comunicação de mudança da residência ao correio</li>
														<p class="mb-0">Antes de mudar de casa, é aconselhável comunicar o novo endereço ao correio local. Durante um ano, cartas endereçadas ao endereço antigo serão entregues ao novo domicílio, sem despesas.
														</p>
													</ul>


