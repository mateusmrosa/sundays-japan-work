														</ul>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 9.2 - Nos casos de crianças estrangeiras</li>
														<p class="mb-0">Não há obrigação de cursar a escola (curso primário e ginasial), Mas, caso deseje fazê—lo, as escolas públicas podem admiti—las, desde que haja vagas. Convém procurar o comitê de ensino da prefeitura, levando os documentos e as próprias crianças.</p>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 9.3 - Sobre o ensino dos filhos no Japão</li>
														<p class="mb-0">É muito natural que todos os membros de uma família morem juntos, seja no Brasil ou no Japão. Queremos, entretanto, levar ao conhecimento dos pais que pretendem ir ao Japão acompanhados de seus filhos menores, sobre certos fatos.
															<br><br>
															Os filhos menores têm a facilidade de se adaptar à nova vida, aprendendo o idioma japonês. Em compensação, facilmente abandonam a cultura brasileira, ao mesmo tempo, esquecem o português.
															<br><br>
															Depois de um período de estadia no Japão, ao regressar ao Brasil, os filhos podem encontrar dificuldades na readaptação á vida no Brasil. De vez em quando aparecem nos jornais noticia de que há escolas mantendo professor que fala português. Mas isso, em relação ao número de filhos brasileiros residentes no Japão, é raro, e a maioria deles não tem chance de receber aulas em português. Assim, toda responsabilidade de manter os filhos estudando a linguá portuguesa é dos pais.
														</p>
													</ul>

												</div>

												<div>
													<p class="mb-0 font-weight-semibold"><b>10 - imposto no Japão</b></p>
													<p class="mb-0">Os principais impostos vigentes no Japão estão relacionados abaixo. Aqueles que tem relação com o Dekasseguis serão explanados à parte.</p>

													<ul>
														<li class="mb-0 font-weight-semibold"> 10.1 - Imposto de renda</li>
														<p class="mb-0">Independente do sistema de cálculo da remuneração do trabalho, seja horista, diarista ou mensalista, sobre o ganho dos dekasseguis recai, o imposto de renda.</p>
														<p class="mb-0">Damos a seguir uma explanação sobre o IR do Japão.</p>
														<ul>
															<li>A base de cálculo é renda de um ano civil.</li>
															<li>O IR dos empregados, os empregadores descontam—no do ordenado mensalmente, e recolhem—no ao cofre do governo.O IR mensal será calculado sobre o possível ganho anual, descontados os benefícios, como despesas de dependentes e contribuições sociais. Na prática aplica—se uma tabela pré estabelecida.</li>
															<li>Naturalmente, pode haver diferença entre o valor devido e o recolhido antecipadamente. Esta diferença pode ser ajustada em dezembro. O ajuste tem nome de “Nenmatesu—Chosei”, cujo significado é acerto no fim do ano” .</li>
															<li>Anualmente, no mês de janeiro, todos os empregados recebem das empresas, um atestado de recolhimento na fonte, denominado ‘Kyuyo-Shotokuno Gensen-Chosyhuhyo”. Este atestado é um documento importante para comprovar seu rendimento do ano anterior e o valor recolhido como IR.Guarde—o com o máximo de cuidado, trazendo—o consigo ao retornar ao pais.</li>
															<li>A alíquota de IR. A alíquota abaixo é aplicável sobre o ganho líquido.</li>
															<li>Liquido anual</li>
															<ul>
																<li>abaixo de ‎¥ 3.000.000 = alíquota de 10%</li>
																<li>de ¥ 3.000.000 a ¥ 6.000.000 = alíquota de 20%</li>
																<li>de ‎¥ 6.000.000 a ‎¥ 10.000.000 = alíquota de 30%</li>
																<li>de ‎¥ 10.000.000 a ‎¥ 20.000.000 = alíquota de 40%</li>
																<li>acima de ‎¥ 20.000.000 = alíquota de 50%</li>
																<li>Alíquota maior será aplicada sobre o valor excedente ao limite da alíquota imediatamente anterior. O sistema é igual ao do Brasil.</li>
															</ul>
															<li>Declaração anual de IR</li>
															<ul>
																<li>Pessoa isenta de apresentar a declaração anual: Quem tenha auferido como rendimento anual até ‎¥ 15.000.000 e que tenha sofrido o ajuste no final de ano.</li>
																<li>B) Pessoa que precisa apresentar:</li>
																<ul>
																	<li>Quem tenha auferido rendas em duas ou mala fontes, e quem tenha recebido de uma outra fonte de renda, maie de ‎¥ 200.000. O prazo da apresentação é de 16 de fevereiro atá 15 de narco. Quem estiver nesta condição pode procurar a delegacia de receita ZEIMUSHO, para receber orientação.</li>
																</ul>
															</ul>
														</ul>
													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 10.2 - Imposto Habitacional - JUMINZEI</li>
														<p class="mb-0">Há dois tipos de juminzei:</p>
														<ul>
															<li>TO-DO-FU-KENMIN-ZEI (KENMINZEI) é da província</li>
															<li>SHI-CHÕ-SONMIN-ZEI (SÁIMINZEI) ã do Município.</li>
														</ul>
														<p class="mb-0">O cálculo do imposto: </p>
														<ul>
															<li>A) Primeira parte, a taxa per capta é uniforme</li>
															<li>B) outra parte é variável, de acordo com a renda.</li>
															<li>O Imposto a pagar é a soma de A + B.</li>
														</ul>
														<p class="mb-0">Aonde pagar e a partir de quando ?</p>
														<ul>
															<li>Paga—se à prefeitura onde reside no dia 1º de janeiro. O dekassegui que chegou ao Japão no curso do ano não precisa pagar imposto habitacional nesse ano, pois no dia primeiro de janeiro do ano
																não residia no Japão, devendo pagá—lo somente a partir do ano seguinte com base na renda do ano passado. Se mudar de residência, tem que acertar o imposto com a prefeitura.</li>
														</ul>
														<p class="mb-0">No caso de empregado: </p>
														<ul>
															<li>Anualmente, o empregador comunica prefeitura o rendimento pago e recolhido na fonte. (veja 10.1.4)
																Baseado no informe, a prefeitura calcula e notifica o valor do imposto a pagar; (KENMINZEI E SHIMINZEI) , ao contribuinte através da empresa.</li>
														</ul>
														<p class="mb-0">A emprega por sua vez, deduz, mensalmente, do ordenado o valor do imposto. Este desconto pode começar no mês de junho até maio do ano seguinte.</p>
														<p class="mb-0">O dekaegegui que regressa ao Brasil, definitiva ou temporariamente, deve providenciar o pagamento do tributo quitando—o de uma só vez, ou na sua ausência, através de procurador.</p>
														<p class="mb-0">Aliquota do KENMINZEI</p>
														<ul>
															<li>A) taxa uniforme de ¥ 700 por pessoa</li>
															<li>B) Variável sobre renda anual de até acima ¥ 5.000.000 = 2% e acima de ¥ 5.000.000 = 4%</li>
														</ul>
														<p class="mb-0">8. Aliquota de SHIMINZEI</p>
														<ul>
															<li>taxa único por ano per capta</li>
															<ul>
																<li>população do município acima de 500.000 habitantes = ¥ 2.500</li>
																<li>de 50.000 a 500.000 habitantes = ¥ 2.000</li>
																<li>abaixo de 50.000 habitantes = ¥ 1.500</li>
															</ul>
															<li>Variável sobre a renda de:</li>
															<ul>
																<li>até ¥ 1.600.000……….. = 3%</li>
																<li>de ¥ 1.600.000 a ¥ 5.000.000……….. = 8%</li>
																<li>acima de ¥ 5.000.000……….= 11%</li>
															</ul>
														</ul>

													</ul>

													<ul>
														<li class="mb-0 font-weight-semibold"> 10.3 - Imposto sobre consumo</li>
														<p class="mb-0">O consumidor deve pagar 3% calculado sobre o preço da mercadoria comprada ou do serviço utilizado.</p>
														<p class="mb-0">Dependendo da mercadoria/serviço e de loja, o imposto pode estar incluso no preço de venda. Na maioria dos casos, a cobrança é á parte. Os preços de livros, cigarros e mercadorias vendidas, na máquina automática de venda, estão com o imposto embutido.</p>
													</ul>

												</div>

												<div>
													<p class="mb-0 font-weight-semibold"><b>11 - Compras</b></p>
													<ul>
														<li class="mb-0 font-weight-semibold"> 11.1 - Funcionamento de lojas e compras</li>
														<p class="mb-0">No Brasil, normalmente, todas as lojas não funcionam aos domingos e feriados. No Japão, as lojas fecham em dia determinado da semana, alternadamente. O fechamento de lojas ocorre com base no “centro comercial” ou de ramos de negócios. Nos 3 primeiros dias do ano, fecham—se todas as lojas.</p>
													</ul>
													<ul>
														<li class="mb-0 font-weight-semibold"> 11.2 - Compras econômicas</li>
														<p class="mb-0">Artigos de uso diário, alimentos, remédios comuns, utensílio de cozinha, etc, podem ser adquiridos mais baratos nos super-mercados (“supa”). Para economizar ou baratear o custo de vida convém aproveitar promoção de vendas publicadas, normalmente, nos jornais.</p>
													</ul>
													<ul>
														<li class="mb-0 font-weight-semibold"> 11.3 - "Kombini" - Lojas de conveniência espalhadas na cidade</li>
														<p class="mb-0">Estas funcionam 24 horas por dia, ou de manhã cedo até alta horas, porém os pregos praticados nem sempre são mais baratos.</p>
													</ul>
													<ul>
														<li class="mb-0 font-weight-semibold"> 11.4 - Artigos de luxo ou de preços altos</li>
														<p class="mb-0">É melhor pedir conselhos/ sugestões dos amigos e/ou vizinhos sobre o modelo, marca e os nomes de lojas, pois os moradores são bem mais informados do que os recém—chegados.</p>
													</ul>
													<ul>
														<li class="mb-0 font-weight-semibold"> 11.5 - Compra na máquina de venda automática</li>
														<p class="mb-0">Encontram—se muito na cidade, as máquinas de vendas automáticas de cigarros, refrigerantes, latas de cerveja, funcionando o dia todo. Também há máquinas de venda de macarrão, hambúrguer, prontos para serem consumidos . Há uma tendência de se proibir colocação de máquinas de venda de bebidas alcoólicas, para evitar consumo pelos menores.</p>
													</ul>
													<ul>
														<li class="mb-0 font-weight-semibold"> 11.6 - Pagamento em dinheiro</li>
														<p class="mb-0">No Japão não é usual o pagamento em cheque. Na maioria das vezes paga—se em dinheiro, atualmente o uso do cartão vem se difundindo naquele pais.</p>
													</ul>
													<ul>
														<li class="mb-0 font-weight-semibold"> 11.7 - Incidência de imposto de consumo</li>
														<p class="mb-0">Incide 3% sobre o preço normal da mercadoria. Chamam—se “SOTOZEI” aqueles que o imposto são cobrados à parte e “UCHI-ZEI”, aqueles que o imposto já está incluso no preço.</p>
													</ul>
												</div>

												<div>
